<template>
  <a-input :value="num" type="number" @change="inputChange">
    <a-select slot="addonAfter" :value="time" @change="change" style="min-width: 70px;">
      <a-select-option v-for="item in durationOptions" :key="item.key" :value="item.key">{{ item.label }}</a-select-option>
    </a-select>
  </a-input>
</template>

<script>
import { formatSeconds } from '@/utils/utils'

// 此组件以秒为传递单位，输入输出都为秒（Number）
export default {
  name: 'DurationInput',
  props: {
    value: {
      type: Number,
      required: true,
    },
  },
  data () {
    // 将传入的秒进行和控件单位转换，显示为最大值（如：传入3600，应该显示60分）
    const f = formatSeconds(this.value)
    let ret = f.arr[0]
    for (let i = 0, len = f.arr.length; i < len; i++) {
      const value = f.arr[i]
      if (value[0] > 0) {
        ret = value
        break
      }
    }
    return {
      num: ret[0],
      time: ret[1],
      durationOptions: [
        { label: '秒', key: 'seconds' },
        { label: '分', key: 'minutes' },
        { label: '小时', key: 'hours' },
        { label: '天', key: 'days' },
        { label: '月', key: 'months' },
        { label: '年', key: 'years' },
      ],
    }
  },
  methods: {
    change (val) {
      this.time = val
      this.emit()
    },
    inputChange (val) {
      const v = val.target.value === '0' ? 1 : val.target.value
      this.num = v || 1
      this.emit()
    },
    emit () {
      let sec = this.$moment.duration(this.num, this.time)
      sec = sec.asSeconds()
      this.$emit('change', sec)
    },
  },
}
</script>
