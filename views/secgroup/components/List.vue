<template>
  <page-list
    :list="list"
    :columns="columns"
    :group-actions="groupActions"
    :single-actions="singleActions"
    :export-data-options="exportDataOptions"
    :expand-config="{ lazy: true, loadMethod: loadRules }"
    :showSearchbox="showSearchbox"
    :showGroupActions="showGroupActions" />
</template>

<script>
import * as R from 'ramda'
import { mapGetters } from 'vuex'
import ColumnsMixin from '../mixins/columns'
import SingleActionsMixin from '../mixins/singleActions'
import ListMixin from '@/mixins/list'
import WindowsMixin from '@/mixins/windows'
import { getTenantFilter } from '@/utils/common/tableFilter'
import globalSearchMixins from '@/mixins/globalSearch'

export default {
  name: 'SecgroupList',
  mixins: [WindowsMixin, ListMixin, globalSearchMixins, ColumnsMixin, SingleActionsMixin],
  props: {
    id: String,
    getParams: {
      type: [Function, Object],
      default: () => ({
        details: true,
      }),
    },
    frontGroupActions: {
      type: Function,
    },
  },
  data () {
    return {
      list: this.$list.createList(this, {
        id: this.id,
        resource: 'secgroups',
        getParams: this.getParam,
        filterOptions: {
          name: {
            label: '名称',
            filter: true,
            formatter: val => {
              return `name.contains("${val}")`
            },
          },
          tenant: getTenantFilter(),
        },
        responseData: this.responseData,
      }),
      exportDataOptions: {
        items: [
          { label: 'ID', key: 'id' },
          { label: '名称', key: 'name' },
          { label: '关联虚拟机', key: 'guest_cnt' },
          { label: '共享范围', key: 'public_scope' },
          { label: '状态', key: 'status' },
          { label: this.$t('dictionary.project'), key: 'tenant' },
        ],
      },
    }
  },
  computed: {
    ...mapGetters(['isAdminMode', 'isDomainMode', 'userInfo']),
    groupActions () {
      const _frontGroupActions = this.frontGroupActions ? this.frontGroupActions.bind(this)() || [] : []
      return _frontGroupActions.concat(
        [
          { index: 0,
            label: '新建',
            permission: 'secgroups_create',
            action: () => {
              this.createDialog('CreateSecgroupDialog', {
                title: '新建',
                onManager: this.onManager,
                refresh: this.refresh,
              })
            },
            meta: () => ({
              buttonType: 'primary',
            }),
          },
          {
            index: 2,
            label: '批量追加规则',
            permission: 'secgroups_performAction',
            action: () => {
              this.createDialog('AddRulesDialog', {
                data: this.list.selectedItems,
                columns: this.columns,
                title: '批量追加规则',
                onManager: this.onManager,
                refresh: this.refresh,
              })
            },
            meta: () => {
              if (this.list.selectedItems.length > 0) {
                if (this.isAdminMode) {
                  return {
                    validate: true,
                  }
                } else {
                  return {
                    validate: this.list.selectedItems.every(val => !val.is_public),
                  }
                }
              }
              return {
                validate: false,
                tooltip: '请选择私有安全组',
              }
            },
          },
          {
            index: 3,
            label: '删除',
            permission: 'secgroups_delete',
            action: () => {
              this.createDialog('DeleteResDialog', {
                vm: this,
                data: this.list.selectedItems,
                columns: this.columns,
                title: '删除',
                onManager: this.onManager,
              })
            },
            meta: (obj) => this.$getDeleteResult(this.list.selectedItems),
          },
        ]
      ).sort((a, b) => a.index - b.index)
    },
  },
  created () {
    this.initSidePageTab('secgroup-detail')
    this.list.fetchData()
  },
  methods: {
    getParam () {
      const ret = {
        ...(R.is(Function, this.getParams) ? this.getParams() : this.getParams),
      }
      if (this.cloudEnv) ret.cloud_env = this.cloudEnv
      return ret
    },
    handleOpenSidepage (row) {
      this.sidePageTriggerHandle(this, 'SecGroupSidePage', {
        id: row.id,
        resource: 'secgroups',
        getParams: this.getParam,
      }, {
        list: this.list,
      })
    },
    async loadRules ({ row }) {
      let manager = new this.$Manager('secgrouprules')
      try {
        const response = await manager.list({
          params: {
            secgroup: row.id,
            scope: this.$store.getters.scope,
            limit: 0,
          },
        })
        row.rules = response.data.data || []
        return response
      } catch (error) {
        throw error
      } finally {
        manager = null
      }
    },
  },
}
</script>
